#import "Weapon.h"

@implementation Weapon

@synthesize weaponName = _weaponName;

-(id) initWithName:(NSString*)aName
{
    if (self = [super init])
    {
        self.weaponName = aName;
    }
    return self;
}

-(void) dealloc
{
    self.weaponName = nil;
    [super dealloc];
}

@end
